package com.alarm.domain;

/**
 * Category response
 */
public class CategoryResponse {

    private String kind;

    private Integer number;

    public String getKind() {
        return kind;
    }

    public void setKind(String kind) {
        this.kind = kind;
    }

    public Integer getNumber() {
        return number;
    }

    public void setNumber(Integer number) {
        this.number = number;
    }
}
